package egovframework.kaist.admin.award.model;

public class AdminAwardRequireVo {

	String idx = "";
	String award_idx = "";
	String require_name = "";
	String require_content = "";
	String create_tm = "";
	String update_tm = "";
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getAward_idx() {
		return award_idx;
	}
	public void setAward_idx(String award_idx) {
		this.award_idx = award_idx;
	}
	public String getRequire_name() {
		return require_name;
	}
	public void setRequire_name(String require_name) {
		this.require_name = require_name;
	}
	public String getRequire_content() {
		return require_content;
	}
	public void setRequire_content(String require_content) {
		this.require_content = require_content;
	}
	public String getCreate_tm() {
		return create_tm;
	}
	public void setCreate_tm(String create_tm) {
		this.create_tm = create_tm;
	}
	public String getUpdate_tm() {
		return update_tm;
	}
	public void setUpdate_tm(String update_tm) {
		this.update_tm = update_tm;
	}
	
}
